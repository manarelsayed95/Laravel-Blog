Route::middleware('auth:api')->get('/user', function (Request $request) {
    return $request->user();
});

//index view  "have table of all posts"
Route::get('/Posts','API\Postcontroller@index')->middleware('auth:sanctum');
//store view to  get data from form then store it into database 
Route::post('/Posts/store', 'API\Postcontroller@store')->middleware('auth:sanctum');
//show view "have all details about one post"
Route::get('/Posts/{post}','API\Postcontroller@show')->middleware('auth:sanctum');


Route::post('/sanctum/token', function (Request $request) {
    $request->validate([
        'email' => 'required|email',
        'password' => 'required',
        'device_name' => 'required'
    ]);

    $user = User::where('email', $request->email)->first();

    if (! $user || ! Hash::check($request->password, $user->password)) {
        throw ValidationException::withMessages([
            'email' => ['The provided credentials are incorrect.'],
        ]);
    }

    return $user->createToken($request->device_name)->plainTextToken;
});